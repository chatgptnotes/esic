
import { useState } from 'react';

export const useDiagnosisComplications = () => {
  const [diagnosisComplications, setDiagnosisComplications] = useState<Record<string, string[]>>({
    'Inguinal Hernia': ['Bowel Obstruction', 'Incarceration', 'Strangulation', 'Chronic Pain'],
    'Hypospadias': ['Urethral Stricture', 'Fistula Formation', 'Meatal Stenosis', 'Chordee Recurrence'],
    'Undescended Testis': ['Testicular Cancer', 'Infertility', 'Testicular Torsion', 'Inguinal Hernia'],
    'Appendicitis': ['Perforation', 'Abscess Formation', 'Sepsis', 'Bowel Obstruction'],
    'Gallbladder Disease': ['Cholangitis', 'Pancreatitis', 'Perforation', 'Common Bile Duct Injury'],
    'Thyroid Disorders': ['Recurrent Laryngeal Nerve Injury', 'Hypoparathyroidism', 'Thyroid Storm', 'Bleeding/Hematoma'],
    'Varicose Veins': ['Deep Vein Thrombosis', 'Pulmonary Embolism', 'Chronic Venous Insufficiency', 'Leg Ulceration'],
    'Hemorrhoids': ['Thrombosis', 'Anal Stricture', 'Incontinence', 'Chronic Bleeding/Anemia'],
    'Urinary Stones': ['Acute Kidney Injury', 'Urosepsis', 'Chronic Kidney Disease', 'Ureteral Stricture'],
    'Phimosis/Circumcision': ['Meatal Stenosis', 'Penile Adhesions', 'Bleeding/Hematoma', 'Infection'],
    'Critical Care/ICU': ['Multi-organ Failure', 'Septic Shock', 'ARDS', 'Nosocomial Infections']
  });

  const handleSaveComplications = (diagnosis: string, complications: string[]) => {
    setDiagnosisComplications(prev => ({
      ...prev,
      [diagnosis]: complications
    }));
  };

  return {
    diagnosisComplications,
    handleSaveComplications
  };
};
